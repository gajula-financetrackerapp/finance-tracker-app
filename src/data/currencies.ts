export function findCurrency(code: string): CurrencyDef | undefined {
  return CURRENCIES.find((c) => c.code === code);
}

/** Prefer distinct symbols where Intl collapses many dollars to "$". */
const SYMBOL_OVERRIDES: Record<string, string> = {
  AUD: 'A$',
  CAD: 'C$',
  SGD: 'S$',
  HKD: 'HK$',
  NZD: 'NZ$',
  TWD: 'NT$',
  MXN: 'MX$',
  ARS: 'AR$',
  CLP: 'CL$',
  COP: 'CO$',
  AED: 'د.إ',
  SAR: '﷼',
};

export function currencyDisplaySymbol(code: string): string {
  if (SYMBOL_OVERRIDES[code]) return SYMBOL_OVERRIDES[code];
  return findCurrency(code)?.sym || code;
}
